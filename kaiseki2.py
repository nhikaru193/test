# --- parse_im920_data 関数（変更なし） ---
def parse_im920_data(data_string):
    """
    IM920からTeratermで受信したと思われるデータ文字列を解析し、
    緯度と経度の小数点表記を返します。
    """
    parts = data_string.strip().split(',')
    if len(parts) < 3:
        return "データ形式が不正です。", None, None

    if ':' in parts[2]:
        payload_start = parts[2].split(':', 1)[1]
    else:
        payload_start = parts[2]

    try:
        data_bytes_str = payload_start + ',' + ','.join(parts[3:])
        # 空の文字列を除外するようにフィルターを強化
        decimal_values = [int(val) for val in data_bytes_str.split(',') if val.strip()]
    except (ValueError, IndexError) as e:
        return f"データ解析エラー: {e}", None, None

    decoded_string = "".join([chr(val) for val in decimal_values])

    try:
        clean_string = decoded_string.strip("() ")
        lat_str, lon_str = clean_string.split(", ")
        latitude = float(lat_str)
        longitude = float(lon_str)
        return "解析成功", latitude, longitude
    except ValueError as e:
        return f"緯度または経度の変換エラー: {e}, デコードされた文字列: '{decoded_string}'", None, None
    except IndexError:
        return f"緯度と経度のデータが見つかりません。デコードされた文字列: '{decoded_string}'", None, None

